package icici.loans.personalloans;

public class UnaryExample 
{

	public static void main(String[] args) 
	{
		int a = 10;
		
		System.out.println(a);
		System.out.println("A++ = " + (a++));
		System.out.println(a);
		System.out.println("++A = " + (++a));
		System.out.println("A-- = " + (a--));
		System.out.println(a);
		System.out.println("--A = " + (--a));
		
	}
}

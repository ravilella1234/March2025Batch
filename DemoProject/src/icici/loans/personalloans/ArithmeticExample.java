package icici.loans.personalloans;

public class ArithmeticExample 
{

	public static void main(String[] args) 
	{
		int a=20, b=10;
		
		System.out.println("A+B =" + (a+b));
		System.out.println("A-B = " + (a-b));
		System.out.println("A*B = " + (a*b));
		System.out.println("A/B = " + (a/b));
		System.out.println("A%B = " + (a%b));
	}

}

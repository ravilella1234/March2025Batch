package icici.loans.personalloans;

public class LogicalExample 
{

	public static void main(String[] args) 
	{
		boolean a = false, b = false;
        System.out.println("a && b: " + (a && b));
        
        
        System.out.println("a || b: " + (a || b));
        
        System.out.println("!a: " + (!a));

	}

}
